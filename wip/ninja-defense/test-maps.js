function testReachability(wave, config) {
  const { size, treasure, map } = config;
  const FLOOR = 0, WALL = 1, TREASURE = 2;
  
  // BFS: 宝物から端まで到達可能か
  const visited = Array(size).fill(0).map(() => Array(size).fill(false));
  const queue = [{x: treasure.x, y: treasure.y}];
  visited[treasure.y][treasure.x] = true;
  
  while (queue.length > 0) {
    const cur = queue.shift();
    const dirs = [{x:0,y:-1},{x:0,y:1},{x:-1,y:0},{x:1,y:0}];
    
    for (const d of dirs) {
      const nx = cur.x + d.x;
      const ny = cur.y + d.y;
      
      if (nx < 0 || ny < 0 || nx >= size || ny >= size) continue;
      if (visited[ny][nx]) continue;
      if (map[ny][nx] === WALL) continue;
      
      visited[ny][nx] = true;
      queue.push({x: nx, y: ny});
    }
  }
  
  // 端タイルに到達可能か確認
  let reachable = false;
  for (let i = 1; i < size - 1; i++) {
    if (visited[1][i] || visited[i][1] || visited[i][size-2] || visited[size-2][i]) {
      reachable = true;
      break;
    }
  }
  
  // 床タイルの割合を計算
  let floorCount = 0;
  let totalCount = 0;
  for (let y = 0; y < size; y++) {
    for (let x = 0; x < size; x++) {
      totalCount++;
      if (map[y][x] !== WALL) floorCount++;
    }
  }
  const floorRatio = (floorCount / totalCount * 100).toFixed(1);
  
  console.log(`Wave ${wave}: ${size}x${size} - Reachable: ${reachable ? '✓' : '✗'} - Floor: ${floorRatio}%`);
  
  return reachable;
}

