let ok = true;
stages.forEach((s, i) => {
  console.log(`\n=== Stage ${i+1} ===`);
  s.guards.forEach((g, j) => {
    if (g.type === 'patrol') {
      let errors = [];
      g.path.forEach((p, k) => {
        const tile = s.map[p.y][p.x];
        if (tile === TILE_TYPE.WALL) {
          errors.push(`waypoint ${k} (${p.x},${p.y}) is WALL`);
          ok = false;
        }
        if (k > 0) {
          const prev = g.path[k-1];
          const dist = Math.abs(p.x - prev.x) + Math.abs(p.y - prev.y);
          if (dist !== 1) {
            errors.push(`waypoint ${k-1}→${k} not adjacent (dist=${dist})`);
            ok = false;
          }
        }
      });
      if (errors.length === 0) {
        console.log(`✓ Guard ${j+1} (patrol): ${g.path.length} waypoints, all valid`);
      } else {
        console.log(`❌ Guard ${j+1} (patrol):`);
        errors.forEach(e => console.log(`   ${e}`));
      }
    } else if (g.type === 'camera') {
      const tile = s.map[g.y][g.x];
      console.log(`✓ Guard ${j+1} (camera) at (${g.x},${g.y}): tile=${tile}`);
    }
  });
});

console.log(ok ? '\n✅ ALL PATHS VALID' : '\n❌ SOME PATHS INVALID');
process.exit(ok ? 0 : 1);
